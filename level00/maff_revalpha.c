#include <unistd.h>

int main(void)
{
	char	z;
	char	y;

	z = 'z';
	y = 'Y';
	while (z >= 'b' && y >= 'A')
	{
		write(1, &z, 1);
		z = z - 2;
		write(1, &y, 1);
		y = y -2;
	}
	write(1, "\n", 1);
}
