char		*ft_strrev(char *str)
{
	char	temp;
	int		b;
	int		i;

	b = 0;
	i = 0;
	while (str[i])
		i++;
	i--;
	while (i >= b)
	{
		temp = str[b];
		str[b] = str[i];
		str[i] = temp;
		b++;
		i--;
	}
	return (str);
}
